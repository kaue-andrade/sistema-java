public class PlanoMensal {
    private String cpf;
    private String nome;
    private int fone;

    public PlanoMensal() {
        this.cpf = cpf;
        this.nome = nome;
        this.fone = fone;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getFone() {
        return fone;
    }

    public void setFone(int fone) {
        this.fone = fone;
    }
}